"""Possible modes for a GPIO pin."""


IN = 0
"""Pin is an input."""

OUT = 1
"""Pin is an input."""

PWM = 2
"""Pin is a PWM (Pulse-Width Modulation) output."""

CLOCK = 3
"""Pin is in clock mode."""

UP = 2
"""Set internal pull-up resistor."""

DOWN = 1
"""Set internal pull-down resistor."""

TRI = 0
"""Set mode to none."""
