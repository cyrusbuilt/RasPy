"""Display on/off controls."""


DISPLAY_ON = 0x04
"""Turn the display on."""

DISPLAY_OFF = 0x00
"""Turn the display off."""

CURSOR_ON = 0x02
"""Turn the cursor on."""

CURSOR_OFF = 0x00
"""Turn the cursor off."""

BLINK_ON = 0x01
"""Turn the cursor blink on."""

BLINK_OFF = 0x00
"""Turn the cursor blink off."""
