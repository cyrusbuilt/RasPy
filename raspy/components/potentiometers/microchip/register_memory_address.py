"""Register memory addresses."""


WIPER0 = 0x00
"""Wiper 0."""

WIPER1 = 0x01
"""Wiper 1."""

WIPER0_NV = 0x02
"""Wiper 0 non-volatile."""

WIPER1_NV = 0x03
"""Wiper 1 non-volatile."""

TCON01 = 0x04
"""Terminal control for wipers 0 and 1."""

WIPER2 = 0x06
"""Wiper 2."""

WIPER3 = 0x07
"""Wiper 3."""

WIPER2_NV = 0x08
"""Wiper 2 non-volatile."""

WIPER3_NV = 0x09
"""Wiper 3 non-volatile."""

TCON23 = 0x04
"""Terminal control for wipers 2 and 3."""

NONE = 0
"""Null bit."""
